number=int(input('Por favor, insira um número:')) #Armazena na variável number o número inteiro digitado após os dois pontos
number1=int(number-1) #Armazena na variável number1 o número inteiro anterior ao que foi digitado acima, ou seja, diminuindo 1
number2=int(number+1) #Armazena na variável number2 o número inteiro posterior ao que foi digitado acima, ou seja, aumentando 1
print('O número antecessor corresponde a {}'.format(number1)) #imprime na tela o valor do número anterior
print('O número posterior corresponde a {}'.format(number2)) #imprime na tela o valor do número posterior